package com.ncu.model;

import java.util.Date;

public class WeekModel {
	private int type;
	private double sum;
	private Date date;
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public double getSum() {
		return sum;
	}
	public void setSum(double sum) {
		this.sum = sum;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	
}
